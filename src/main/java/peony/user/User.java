package peony.user;

public abstract interface User {
	public abstract String getEmailAddress();
	public abstract String getFirstName();
	public abstract String getLastName();
	public abstract String getHomeAddress();
	public abstract String getHomeCity();
	public abstract String getHomeState();
	public abstract String getHomeZip();
	public abstract String getHomePhoneNumber();
	public abstract String getBirthYear();
}
